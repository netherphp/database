<?php

namespace Nether\Database\Coda;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

use Nether\Common;

use Stringable;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

class Equals
implements
	Stringable,
	Common\Interfaces\ToString {

	use
	Common\Package\StringableAsToString;

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public string
	$Field;

	public string
	$Value;

	public bool
	$Is;

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public function
	__Construct(string $Field, string $Value, bool $Is=TRUE) {

		$this->Field = $Field;
		$this->Value = $Value;
		$this->Is = $Is;

		return;
	}

	////////////////////////////////////////////////////////////////
	// IMPLEMENTS Common\Interfaces ////////////////////////////////

	public function
	ToString():
	string {

		return sprintf(
			'%s%s%s',
			$this->Field,
			($this->Is ? '=' : '!='),
			$this->Value
		);
	}

}
